<?php

/**
 * Suma dos números enteros
 *
 * Esta función toma dos números enteros como parámetros y devuelve su suma.
 *
 * @param int $n1 El primer número entero.
 * @param int $n2 El segundo número entero.
 * @return int La suma de los dos números enteros.
 */
function suma($n1, $n2){
    return $n1 + $n2;
}
